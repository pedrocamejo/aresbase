package cpc.ares.reportes;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;



public class Filtro implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -3975717068734617592L;
	private int 					idFiltro;
	private	int						idReporte;
	private List<DataFiltro> 		filtro;
	
	public Filtro(){
		filtro = new ArrayList<DataFiltro>();
	}

	public int getIdFiltro() {
		return idFiltro;
	}
	public void setIdFiltro(int idFiltro) {
		this.idFiltro = idFiltro;
	}
	public int getIdReporte() {
		return idReporte;
	}
	public void setIdReporte(int idReporte) {
		this.idReporte = idReporte;
	}
	public List<DataFiltro> getFiltro() {
		return filtro;
	}
	public void setFiltro(List<DataFiltro> filtro) {
		this.filtro = filtro;
	}
}

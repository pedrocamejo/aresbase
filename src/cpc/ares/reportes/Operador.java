package cpc.ares.reportes;

import java.io.Serializable;

public class Operador implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -3137329525107223346L;
	private int			id;
	private String		valorSqlInicio;
	private String		valorSqlFin;
	private String		valorCombo;
	private boolean 	aplicarLateral;
	
	public Operador() {
		super();
	}
	
	public Operador(int id, String valorSql, String valorCombo) {
		super();
		this.id = id;
		this.valorSqlInicio = valorSql;
		this.valorCombo = valorCombo;
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getValorCombo() {
		return valorCombo;
	}
	
	public void setValorCombo(String valorCombo) {
		this.valorCombo = valorCombo;
	}
	
	public static long getSerialVersionUID() {
		return serialVersionUID;
	}

	public String getValorSqlInicio() {
		return valorSqlInicio;
	}

	public void setValorSqlInicio(String valorSqlInicio) {
		this.valorSqlInicio = valorSqlInicio;
	}

	public String getValorSqlFin() {
		return valorSqlFin;
	}

	public void setValorSqlFin(String valorSqlFin) {
		this.valorSqlFin = valorSqlFin;
	}

	public boolean isAplicarLateral() {
		return aplicarLateral;
	}

	public void setAplicarLateral(boolean aplicarLateral) {
		this.aplicarLateral = aplicarLateral;
	}
	
	public String toString(){
		return valorCombo;
	}
}
